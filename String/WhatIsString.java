package String;

public class WhatIsString 
{    public static void main(String[] args) 
    {
       //String as datatype
       String s1="Rajan";

       //String as class
       String s2=new String("Kushwah");

       System.out.println(s1);
       System.out.println(s2);
        
       //Only addition operator works in string
       System.out.println("hello, "+s1);
    }
}
